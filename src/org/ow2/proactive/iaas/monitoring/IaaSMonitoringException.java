package org.ow2.proactive.iaas.monitoring;

public class IaaSMonitoringException extends Exception {

	public IaaSMonitoringException() {
	}

	public IaaSMonitoringException(String message) {
		super(message);
	}

	public IaaSMonitoringException(Throwable cause) {
		super(cause);
	}

	public IaaSMonitoringException(String message, Throwable cause) {
		super(message, cause);
	}

}
