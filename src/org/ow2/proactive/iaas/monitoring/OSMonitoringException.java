package org.ow2.proactive.iaas.monitoring;

public class OSMonitoringException extends Exception {

	public OSMonitoringException() {
	}

	public OSMonitoringException(String message) {
		super(message);
	}

	public OSMonitoringException(Throwable cause) {
		super(cause);
	}

	public OSMonitoringException(String message, Throwable cause) {
		super(message, cause);
	}

}
